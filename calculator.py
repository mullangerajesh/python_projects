n1=int(input("enter 1st number: "))
n2=int(input("enter 2nd number: "))
opt=input("enter a operator :")
def add():   
   if opt==("+") :
      print(n1+n2)
   
def sub():
   if opt==("-") :
      print(n1-n2)
  
def mul():
   if opt==("*"):
      print(n1*n2)
                      
def div():
   if opt==("/") :
      print(n1/n2)
   
add()
sub()
mul()
div()
   


